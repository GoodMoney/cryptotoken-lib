
class Token:
    def __init__(self, web3, contract_address):
        self.web3 = web3
        self.address = contract_address
        self.contract = self.web3.eth.contract(address=address, abi=erc20_abi)

    def name(self):
        return self.contract.functions.name().call()

    def symbol(self):
        return self.contract.functions.symbol().call()

    def decimals(self):
        return self.contract.functions.decimals().call()

    def totalSupply(self):
        return self.contract.functions.totalSupply().call()
    
    def balanceOf(self, addr):
        checksum_addr = self.web3.toChecksumAddress(addr)
        wei_balance = self.contract.functions.balanceOf(checksum_addr).call()
        return wei_balance / 10**self.decimals()

    def transfer(self, to, tokens):
        return self.contract.functions.transfer(
            self.web3.toChecksumAddress(to), 
            int(tokens * 10**self.decimals())
        ).buildTransaction()
    


